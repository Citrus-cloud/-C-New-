#pragma once
#include "tuning.h"
// ============================================================================
//  director.h — МЕНЕДЖЕР ВОЛН И ДОСТУПНОСТИ ПРИЁМОВ (Фаза 1, Шаг 3-4)
// ----------------------------------------------------------------------------
//  WaveDirector — «дирижёр» забега. Он держит время забега и решает,
//  какие приёмы врагам сейчас разрешены (по времени разблокировки и
//  кулдауну из tuning.h), а также подсказывает текущий темп волн.
//  В следующих фазах враги будут спрашивать здесь «можно ли сейчас»,
//  вместо хранения собственных таймеров.
// ============================================================================
class WaveDirector {
public:
    float elapsed;   // время текущего забега, сек

    WaveDirector();
    void Reset();            // сброс при старте нового забега
    void Update(float dt);   // продвигает время забега

    // --- Темп волн (значения берутся из Tuning) ---
    float SpawnInterval() const;  // текущий интервал между волнами
    int   WaveCount() const;      // текущий размер волны

    // --- Доступность приёмов ---
    bool  IsUnlocked(Tuning::AbilityId id) const;     // доступен ли по времени
    bool  CanUse(Tuning::AbilityId id) const;         // доступен И кулдаун прошёл
    void  NotifyUsed(Tuning::AbilityId id);           // отметить факт использования (запуск кулдауна)
    float CooldownLeft(Tuning::AbilityId id) const;   // сколько осталось до готовности, сек (0 = готов)

private:
    float lastUsed[Tuning::ABILITY_COUNT]; // время последнего использования каждого приёма
};
