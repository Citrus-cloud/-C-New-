#pragma once
#include "raylib.h"
#include <string>
#include <unordered_map>

// =====================================================================
// TextureManager (Шаг 1)
// Менеджер текстур: загружает каждый PNG только один раз и хранит его в кэше.
// При повторном запросе того же пути отдаёт уже загруженную текстуру —
// это экономит память и время.
// Если файла нет на диске, возвращает безопасную "заглушку" (1x1 белый
// пиксель), чтобы игра не падала, а код мог откатиться на рисование
// цветным прямоугольником (как в v0.1).
// Сделано с заделом на масштабирование: один менеджер на всю игру,
// сюда же позже добавим спрайты врагов, боссов, тайлы и эффекты.
// =====================================================================
class TextureManager
{
public:
    // Получить текстуру по пути. Первый раз грузит с диска и кладёт в кэш,
    // дальше отдаёт из кэша.
    Texture2D Get(const std::string& path);

    // true, если по пути есть реальный файл (а не заглушка).
    bool IsReal(const std::string& path);

    // Выгрузить все загруженные текстуры из памяти (перед закрытием игры).
    void UnloadAll();

private:
    std::unordered_map<std::string, Texture2D> cache; // путь -> текстура
    std::unordered_map<std::string, bool> real;       // путь -> реальный файл?
    Texture2D fallback{};       // общая заглушка 1x1
    bool fallbackReady = false;

    // Создаёт заглушку один раз при первой необходимости.
    void EnsureFallback();
};
