#pragma once
#include "raylib.h"
#include <vector>
// ============================================================================
//  hazards.h — ЗОНЫ-ЛУЖИ УРОНА СО ВРЕМЕНЕМ (Фаза 5, Шаг 27)
// ----------------------------------------------------------------------------
//  Лёгкая система ПЕРСИСТЕНТНЫХ опасных зон на земле: ядовитый след, кислотные
//  лужи и т.п. В отличие от телеграфа (однократный удар по завершении), лужа
//  живёт заданное время и бьёт игрока ПЕРИОДИЧЕСКИ, пока он стоит внутри.
//  Все параметры берутся из tuning.h — никаких «магических чисел» здесь.
// ============================================================================

class Player;   // форвард: урон наносим игроку, не тянем весь заголовок

// Одна опасная зона (лужа). Живёт в пуле по флагу active.
struct Hazard {
    bool active = false;
    Vector2 pos = { 0.0f, 0.0f };
    float radius = 0.0f;     // радиус зоны, пикс
    int   damage = 0;        // урон за один тик
    float tick = 0.5f;       // период нанесения урона, сек
    float tickTimer = 0.0f;  // отсчёт до следующего тика
    float life = 0.0f;       // остаток времени жизни, сек
    float maxLife = 1.0f;    // полное время жизни (для прозрачности при затухании)
    Color color = GREEN;
};

// Пул опасных зон. Враги (через спавнер) «роняют» лужи методом Spawn.
class HazardSystem {
public:
    std::vector<Hazard> pool;

    HazardSystem(int poolSize = 96);
    Hazard* GetInactive();

    // Создать лужу: позиция, радиус, урон за тик, период тика, время жизни, цвет.
    Hazard* Spawn(Vector2 pos, float radius, int damage, float tick, float life, Color c);

    // Продвигает таймеры жизни/тиков; периодически бьёт игрока, если он в зоне.
    void Update(float dt, Player& player);
    void Clear();
    void Draw() const;        // заливка зон по земле (под сущностями)
    void DrawDebug() const;   // отладка: яркие контуры зон
    int  ActiveCount() const;
};
